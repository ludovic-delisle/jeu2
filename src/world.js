nodes = []
function build_world(){
    let canvas = d3.select("#main_svg")

    let width = 80
    let height = 80
    let mass_0= 20000

    for(let i = 0; i< world.length ; i++){
        for(let j = 0; j< world[i].length; j++){
            switch(world[i][j]){
                case 0:
                    break;
                case 1:
                    canvas.append("rect")
                        .attr("x", width*j)
                        .attr("y", height*i)
                        .attr("width", width)
                        .attr("height", height)
                        .attr("class", "decor 1")

                    nodes.push({
                        x: width*j,
                        y: height*i,
                        vx: 0,
                        vy: 0,
                        mass: mass_0,
                        size: width,
                        fx:width*j,
                        fy:width*i
                    })
                    break;
            }
        }
    }
    let rects = d3.selectAll(".decor")
    rects.data(nodes)
    return(nodes)
}

function limit_x(x){
    if(nodes[0].x > -x){return true}

    return false
}
function limit_y(x){
    if(nodes[0].y > -x){return true}

    return false
}

function translate_world(x, y){

    nodes.forEach(element => element.fx+=x)
    nodes.forEach(element => element.fy+=y)
    nodes.forEach(element => element.x+=x)
    nodes.forEach(element => element.y+=y)


}