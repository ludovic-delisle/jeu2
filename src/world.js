data = []
function build_world(){
    let canvas = d3.select("#main_svg")

    let width = 80
    let height = 80

    for(let i = 0; i< world.length ; i++){
        for(let j = 0; j< world[i].length; j++){
            switch(world[i][j]){
                case 0:
                    break;
                case 1:
                    canvas.append("rect")
                        .attr("x", width*j)
                        .attr("y", height*i)
                        .attr("width", width)
                        .attr("height", height)
                        .attr("class", "decor 1")

                    data.push({
                        x: width*j,
                        y: height*i,
                        width: width,
                        height: height})
                    break;
            }
        }
    }
    let rects = d3.selectAll(".decor")
    rects.data(data)

}

function limit_x(x){
    if(data[0].x > -x){return true}

    return false
}
function limit_y(x){
    if(data[0].y > -x){return true}

    return false
}

function translate_world(x, y){

    data.forEach(element => element.x+=x)
    data.forEach(element => element.y+=y)

    let rects = d3.selectAll(".decor")

    rects.data(data)



    rects.attr("x", function(d){return d.x})
        .attr("y", function(d){return d.y})

}